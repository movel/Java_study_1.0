package theme2.Issue1;

public class Plant {
	String name;
	double size;
	double weight;
	String phyta;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getSize() {
		return size;
	}

	public void setSize(double size) {
		this.size = size;
	}

	public double getWeight() {
		return weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}

	public String getPhyta() {
		return phyta;
	}

	public void setPhyta(String phyta) {
		this.phyta = phyta;
	}

}
